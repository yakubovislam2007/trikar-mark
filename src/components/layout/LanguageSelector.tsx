import React from 'react';
import { useLanguage } from '@/contexts/LanguageContext';
import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuItem,
  DropdownMenuTrigger,
} from '@/components/ui/dropdown-menu';
import { Button } from '@/components/ui/button';
import { Globe } from 'lucide-react';

const languages = [
  { code: 'ru', label: 'Ð ÑƒÑÑÐºÐ¸Ð¹', flag: 'ðŸ‡·ðŸ‡º' },
  { code: 'kz', label: 'ÒšÐ°Ð·Ð°Ò›ÑˆÐ°', flag: 'ðŸ‡°ðŸ‡¿' },
  { code: 'en', label: 'English', flag: 'ðŸ‡¬ðŸ‡§' },
] as const;

interface LanguageSelectorProps {
  variant?: 'default' | 'auth';
}

export const LanguageSelector = ({ variant = 'default' }: LanguageSelectorProps) => {
  const { language, setLanguage } = useLanguage();
  const currentLang = languages.find(l => l.code === language);

  return (
    <DropdownMenu>
      <DropdownMenuTrigger asChild>
        <Button 
          variant={variant === 'auth' ? 'ghost' : 'outline'} 
          size="sm"
          className={variant === 'auth' ? 'text-primary-foreground hover:bg-primary/20' : ''}
        >
          <Globe className="h-4 w-4 mr-2" />
          <span>{currentLang?.flag} {currentLang?.code.toUpperCase()}</span>
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="end">
        {languages.map((lang) => (
          <DropdownMenuItem
            key={lang.code}
            onClick={() => setLanguage(lang.code)}
            className={language === lang.code ? 'bg-accent' : ''}
          >
            <span className="mr-2">{lang.flag}</span>
            {lang.label}
          </DropdownMenuItem>
        ))}
      </DropdownMenuContent>
    </DropdownMenu>
  );
};
